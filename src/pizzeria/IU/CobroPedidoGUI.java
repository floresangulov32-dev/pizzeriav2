    private void cargarResumenCobro() {
       
        pizzeria.model.Venta venta = ContextoVentasGUI.getInstancia()
                .getGestorVenta()
                .getVentaActual();

        javax.swing.table.DefaultTableModel modelo =
                (javax.swing.table.DefaultTableModel) tblResumenCobro.getModel();

        modelo.setRowCount(0);

        if (venta == null) {
            lblSubtotalCobro.setText("Bs. 0.00");
            lblTotalCobro.setText("Bs. 0.00");
            lblCambioCobro.setText("Bs. 0.00");
            return;
        }

        for (pizzeria.model.DetalleVenta detalle : venta.getItems()) {
            modelo.addRow(new Object[]{
                detalle.getProducto().getNombre() + " x" + detalle.getCantidad(),
                "Bs. " + String.format("%.2f", detalle.getSubTotal())
            });
        }

        for (pizzeria.model.DetalleCombo detalleCombo: venta.getCombos()) {

            String detalleComboTexto =
                    "Combo #"
                    + detalleCombo.getNroCombo()
                    + " x"
                    + detalleCombo.getCantidad();

            if (detalleCombo.getDescripcion() != null
                    && !detalleCombo.getDescripcion()
                            .trim()
                            .isEmpty()) {

                detalleComboTexto += " - "
                        + detalleCombo.getDescripcion();
        }

        modelo.addRow(new Object[]{
            detalleComboTexto,
            "Bs. "
                    + String.format(
                            "%.2f",
                            detalleCombo.getSubTotal()
                    )
        });
    }

        venta.calcularTotal();

        lblSubtotalCobro.setText("Bs. " + String.format("%.2f", venta.getTotal()));
        lblTotalCobro.setText("Bs. " + String.format("%.2f", venta.getTotal()));
        lblCambioCobro.setText("Bs. 0.00");
    }
    
    
    
    
    
    
    
    
    private void calcularCambio() {
                pizzeria.model.Venta venta = ContextoVentasGUI.getInstancia()
            .getGestorVenta()
            .getVentaActual();

    if (venta == null) {
        lblCambioCobro.setText("Bs. 0.00");
        return;
    }

    venta.calcularTotal();

    if (!rbEfectivo.isSelected()) {
        lblCambioCobro.setText("Bs. 0.00");
        return;
    }

    try {
        String texto = txtMontoRecibido.getText().trim();

        if (texto.isEmpty()) {
            lblCambioCobro.setText("Bs. 0.00");
            return;
        }

        double montoRecibido = Double.parseDouble(texto);
        double cambio = montoRecibido - venta.getTotal();

        if (cambio < 0) {
            lblCambioCobro.setText("Bs. 0.00");
        } else {
            lblCambioCobro.setText("Bs. " + String.format("%.2f", cambio));
        }

    } catch (NumberFormatException e) {
        lblCambioCobro.setText("Bs. 0.00");
    }
    }
    
    private void configurarEventosCobro() {
            txtMontoRecibido.addKeyListener(new java.awt.event.KeyAdapter() {
        @Override
        public void keyReleased(java.awt.event.KeyEvent e) {
            calcularCambio();
        }
        });

        rbEfectivo.addActionListener(e -> {
            txtMontoRecibido.setEnabled(true);
            calcularCambio();
        });

        rbQR.addActionListener(e -> {
            txtMontoRecibido.setText("");
            txtMontoRecibido.setEnabled(false);
            calcularCambio();
        });

        rbTarjeta.addActionListener(e -> {
            txtMontoRecibido.setText("");
            txtMontoRecibido.setEnabled(false);
            calcularCambio();
        });
    }
    
    private pizzeria.model.MetodoPago obtenerMetodoPagoSeleccionado() {
        if (rbQR.isSelected()) {
            return pizzeria.model.MetodoPago.QR;
        }

        if (rbTarjeta.isSelected()) {
            return pizzeria.model.MetodoPago.TARJETA;
        }

        return pizzeria.model.MetodoPago.EFECTIVO;
    }
    
    private void cargarImagen(JLabel label, String ruta){
     label.setText("");
    try {
        java.io.File archivo = new java.io.File(ruta);
        if (!archivo.exists()) {
            System.err.println("Imagen no encontrada: " + ruta);
            return;
        }
        ImageIcon icono = new ImageIcon(archivo.getAbsolutePath());
        Image imgEscalada = icono.getImage().getScaledInstance(
            label.getPreferredSize().width,
            label.getPreferredSize().height,
            Image.SCALE_SMOOTH
        );
        label.setIcon(new ImageIcon(imgEscalada));
    } catch (Exception e) {
        System.err.println("Error cargando imagen: " + e.getMessage());
    }
    }
    
