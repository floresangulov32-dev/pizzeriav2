    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText().trim();
        String unidad = getUnidadSeleccionada();
        String stockStr = txtPrecio.getText().trim();      // Stock Inicial
        String minStr = txtDescrip1.getText().trim();      // Stock Mínimo
        String precioStr = txtDescrip.getText().trim();    // Precio
        String cantStr = txtDescrip2.getText().trim();     // Cantidad por Pizza

    if (nombre.isEmpty() || unidad.isEmpty() || stockStr.isEmpty() ||
        minStr.isEmpty() || precioStr.isEmpty() || cantStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Complete todos los campos.");
        return;
    }
    try {
        double stock = Double.parseDouble(stockStr);
        double min = Double.parseDouble(minStr);
        double precio = Double.parseDouble(precioStr);
        double cant = Double.parseDouble(cantStr);

        Insumo nuevo = inventario.agregar(nombre, unidad, stock, min, precio, cant);
        if (nuevo != null) {
            inventario.guardarArchivo();
            javax.swing.JOptionPane.showMessageDialog(this, "Insumo agregado correctamente.");
            padre.mostrarTabla();
            padre.getContentPane().remove(this);
            padre.revalidate();
            padre.repaint();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Ya existe un insumo con ese nombre.");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Los valores numéricos no son válidos.");
    }
    }//GEN-LAST:event_btnGuardarActionPerformed
    
    private String getUnidadSeleccionada() {
        if (jCheckBox5.isSelected()) return "kg";
        if (jCheckBox6.isSelected()) return "gr";
        if (jCheckBox7.isSelected()) return "ml";
        if (jCheckBox8.isSelected()) return "lt";
        if (jCheckBox9.isSelected()) return "unidad";
        if (jCheckBox10.isSelected()) return "porción";
        if (jCheckBox11.isSelected()) return "caja";
        if (jCheckBox12.isSelected()) return "docena";
        if (!jTextField2.getText().trim().isEmpty()) return jTextField2.getText().trim();
        return "";
    }
    
