    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int fila = jTable1.getSelectedRow();
        if (fila < 0) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Seleccione un producto de la lista.",
                "Sin selección", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        Producto seleccionado = menu.getProductos().get(fila);

        

        productosSeleccionados.add(seleccionado);
        refrescarTablaCombo();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnVolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVolverActionPerformed
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this,
            "¿Descartar el combo en progreso?",
            "Volver", javax.swing.JOptionPane.YES_NO_OPTION,
            javax.swing.JOptionPane.QUESTION_MESSAGE);

        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            if (onComboAgregado != null) {
                onComboAgregado.run(); // reutiliza el callback para volver
            }
        }
        cargarPanel(new PVerCombos(interfaz, menu, archivoMenu, inventario));
    }//GEN-LAST:event_btnVolverActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (productosSeleccionados.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Agregue al menos un producto al combo.",
                "Combo vacío", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validar precio
        double precio;
        try {
            precio = Double.parseDouble(PrecioE.getText().trim().replace(",", "."));
            if (precio <= 0) throw new NumberFormatException();
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Ingrese un precio válido (número positivo).",
                "Precio inválido", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear y guardar el combo
        menu.agregarCombo(new ArrayList<>(productosSeleccionados), precio);

        if (archivoMenu != null) {
            archivoMenu.guardarCombos(menu.getCombos());
        }

        javax.swing.JOptionPane.showMessageDialog(this,
            "Combo agregado correctamente.",
            "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        // Navega directo, el nuevo PVerCombos llama cargarTablaCombos() en su constructor
        
        // Limpiar y notificar
        productosSeleccionados.clear();
        refrescarTablaCombo();
        PrecioSug.setSelected(true);
        PrecioE.setEnabled(false);
        PrecioE.setText("0.00");

        PVerCombos panelCombos = new PVerCombos(interfaz, menu, archivoMenu, inventario);
        cargarPanel(panelCombos);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         int fila = jTable2.getSelectedRow();

        if (fila < 0) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Seleccione un producto del combo para eliminar.",
                "Sin selección",
                javax.swing.JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int opcion = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "¿Desea eliminar este producto del combo?",
            "Confirmar",
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (opcion == javax.swing.JOptionPane.YES_OPTION) {
            productosSeleccionados.remove(fila);
            refrescarTablaCombo();
        }
    
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void configurarRadioButtons() {
        // Por defecto precio sugerido activo, campo deshabilitado
        PrecioSug.setSelected(true);
        PrecioE.setEnabled(false);
        PrecioE.setBackground(new java.awt.Color(217, 217, 217));

        // Solo habilita el campo cuando se elige Precio Especial
        PrecioSug.addActionListener(e -> {
            PrecioE.setEnabled(false);
            PrecioE.setBackground(new java.awt.Color(217, 217, 217));
            PrecioE.setText(calcularPrecioSugerido());
        });

        PrecioEsp.addActionListener(e -> {
            PrecioE.setEnabled(true);
            PrecioE.setBackground(new java.awt.Color(245, 245, 245));
            PrecioE.requestFocus();
        });
    }
    
    private void cargarTablaProductos() {
        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        if (menu == null) return;

        for (Producto p : menu.getProductos()) {
            model.addRow(new Object[]{
                p.getID(),
                p.getNombre(),
                String.format("%.2f Bs.", p.getPrecio())
            });
        }
    }
    
    private void refrescarTablaCombo() {
        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        for (Producto p : productosSeleccionados) {
            model.addRow(new Object[]{p.getID(), p.getNombre()});
        }

        // Actualiza precio sugerido si está seleccionado
        if (PrecioSug.isSelected()) {
            PrecioE.setText(calcularPrecioSugerido());
        }
    }
    
    private String calcularPrecioSugerido() {
        double total = productosSeleccionados.stream()
            .mapToDouble(Producto::getPrecio)
            .sum();
        return String.format("%.2f", total);
    }
    
    private void cargarPanel(JPanel panel) {
    interfaz.removeAll();
    interfaz.setLayout(new BorderLayout());
    interfaz.add(panel, BorderLayout.CENTER);
    interfaz.revalidate();
    interfaz.repaint();
    }
    

