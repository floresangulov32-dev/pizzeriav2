private void configurarPlaceholder() {
        idBuscar.setText("Ingrese un Nro de Combo:");
        idBuscar.setForeground(java.awt.Color.GRAY);

        idBuscar.addFocusListener(new java.awt.event.FocusAdapter() {

    @Override
    public void focusGained(java.awt.event.FocusEvent e) {
        if (idBuscar.getText().equals("Ingrese un Nro de Combo:")) {
            idBuscar.setText("");
            idBuscar.setForeground(new java.awt.Color(0,0,0));
        }
    }

    @Override
    public void focusLost(java.awt.event.FocusEvent e) {
        if (idBuscar.getText().trim().isEmpty()) {
            idBuscar.setText("Ingrese un Nro de Combo:");
            idBuscar.setForeground(new java.awt.Color(217,217,217));
        }
    }
});// código anterior
}
    
private void cargarPanel(JPanel panel) {
    interfaz.removeAll();
    interfaz.setLayout(new BorderLayout());
    interfaz.add(panel, BorderLayout.CENTER);
    interfaz.revalidate();
    interfaz.repaint();
}

private void cargarTablaCombos() {
    DefaultTableModel modelo =
        (DefaultTableModel) jTable1.getModel();

    modelo.setRowCount(0);

    for (Combo c : menu.getCombos()) {

        String productos = "";

        for (int i = 0; i < c.getCombo().size(); i++) {
            productos += c.getCombo().get(i).getNombre();

            if (i < c.getCombo().size() - 1) {
                productos += ", ";
            }
        }

        modelo.addRow(new Object[]{
            "Combo #" + c.getNroCombo(),
            productos,
            c.getPrecio()
        });
    }

    jTable1.getColumnModel().getColumn(0).setPreferredWidth(100);
    jTable1.getColumnModel().getColumn(1).setPreferredWidth(300);
    jTable1.getColumnModel().getColumn(2).setPreferredWidth(80);
}
