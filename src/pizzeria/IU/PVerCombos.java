    private void btnEliminarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(
                this,
                "Seleccione un combo de la tabla."
            );
            return;
        }

        String combo = jTable1.getValueAt(fila, 0).toString();

        // Si en la columna 0 guardas "Combo #1"
        int nroCombo = Integer.parseInt(
            combo.replace("Combo #", "").trim()
        );

        int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Desea eliminar el " + combo + "?",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION
        );

        if (opcion == JOptionPane.YES_OPTION) {

            if (menu.eliminarCombo(nroCombo)) {

                if (archivoMenu != null) {
                    archivoMenu.guardarCombos(menu.getCombos());
                }

                cargarTablaCombos();

                JOptionPane.showMessageDialog(
                    this,
                    "Combo eliminado correctamente."
                );
            }
        }
    }//GEN-LAST:event_btnEliminarCActionPerformed

