    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            int nroCombo = Integer.parseInt(idBuscar.getText());

            Combo c = menu.buscarCombo(nroCombo);

            if (c == null) {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "No se encontró un Combo con ese número."
                );
                return;
            }

            DefaultTableModel modelo =
                (DefaultTableModel) jTable1.getModel();

            modelo.setRowCount(0);

            String productos = "";

            for (int i = 0; i < c.getCombo().size(); i++) {
                productos += c.getCombo().get(i).getNombre();

                if (i < c.getCombo().size() - 1) {
                    productos += ", ";
                }
            }

            modelo.addRow(new Object[]{
                "Combo #" + c.getNroCombo(),
                productos,
                String.format("%.2f", c.getPrecio())
            });

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Ingrese un número válido."
            );
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

