    private void mostrarUsuario() {
    
         if (rolUsuario == null || rolUsuario.trim().isEmpty()) {
        rolUsuario = "Cajero";
    }

    if (nombreUsuario == null) {
        nombreUsuario = "";
    }

    if (nombreUsuario.trim().isEmpty()) {
        Rol.setText(rolUsuario);
    } else {
        Rol.setText(rolUsuario + ": " + nombreUsuario);
    }
    }
    
